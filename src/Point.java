/**
 * Point class that provides x, y coordinates
 * @author TerryNguyen
 *
 */
public class Point {
	/**
	 * x coordinates
	 */
	 int xCoor = 0;
	 /**
	  * y coordinates
	  */
	 int yCoor = 0;
	/**
	 * Point constructor
	 * @param x coordinates
	 * @param y coordinates
	 */
	Point(int x, int y){
		xCoor = x;
		yCoor = y;
	}
	
}
